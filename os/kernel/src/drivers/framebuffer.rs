/// JARVIS color theme (BGR format)
struct JarvisColors;
impl JarvisColors {
    const BG: [u8; 3] = [0x1A, 0x0A, 0x0A];        // Dark blue-black (#0A0A1A)
    const FG: [u8; 3] = [0xBA, 0x8A, 0x4A];         // JARVIS cyan (#4A8ABA)
    const ACCENT: [u8; 3] = [0x8A, 0x5A, 0x2A];     // Darker blue (#2A5A8A)
    const ERROR: [u8; 3] = [0x4A, 0x4A, 0xEA];      // Red (#EA4A4A)
}

